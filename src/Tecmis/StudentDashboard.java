    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
        //courseDetails.setText("");
        String CID=(String)Acourses.getSelectedItem();
        try {

            insert=conn1.prepareStatement("select *  from attendance where atte_cmod_id='"+CID+"' and atte_st_id='"+userID+"'");
            ResultSet rs= insert.executeQuery();
  
            DefaultTableModel df=(DefaultTableModel)ATTable.getModel();            
            double tothourset = 0,tothoursep = 0,atothourset=0,atothoursep=0;

            PProgress.setVisible(false);
            jLabel53.setVisible(false);
            PPre.setVisible(false);

            TProgress.setVisible(false);
            jLabel49.setVisible(false);
            Tpre.setVisible(false);

            df.setRowCount(0);
            while(rs.next()){
                Vector v1=new Vector();
                    int Hourse=rs.getInt("Hourse");
                    String ACon=rs.getString("atte_condition");
                    int ALevel=rs.getInt("atte_level");             
                    v1.add(rs.getString("atte_id"));
                    v1.add(rs.getString("atte_date"));
                    v1.add(ACon);
                    v1.add(ALevel);
                    v1.add(Hourse);
                    if("T".equals(ACon)){
                        TProgress.setVisible(true);
                        jLabel49.setVisible(true);
                        Tpre.setVisible(true);
                        tothourset=tothourset+2;
                        if(ALevel==1){
                            atothourset=atothourset+2;
                         }
                     }else{
                        PProgress.setVisible(true);
                        jLabel53.setVisible(true);
                        PPre.setVisible(true);
                        tothoursep=tothoursep+3;
                        if(ALevel==1){
                            atothoursep=atothoursep+3;
                         }
                    }

                    df.addRow(v1);
             }
                double pTheory=(atothourset/tothourset)*100;
                double pPractical=(atothoursep/tothoursep)*100;
                double totattendance=(atothourset+atothoursep)/(tothourset+tothoursep)*100;

                    for(int i=0;i<=pTheory;i++){
                        TProgress.setValue(i);
                        
                        Tpre.setText("Precentage "+Integer.toString(i)+"%");
                        }

                    for(int i=0;i<=pPractical;i++){
                         PProgress.setValue(i);
                        PPre.setText("Precentage "+Integer.toString(i)+"%");
                    }

                    for(int i=0;i<=totattendance;i++){
                         totProgress.setValue(i);
                        totPre.setText("Precentage "+Integer.toString(i)+"%");
                        if(i>80){
                            totProgress.setStringPainted(true);
                            totProgress.setString("ELIGIBLE");
                        }else{
                            totProgress.setStringPainted(true);
                            totProgress.setString("NOT ELIGIBLE");
                            }
                    }



                } catch (SQLException ex) {
                    Logger.getLogger(StudentDashboard.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_jButton6MouseClicked

