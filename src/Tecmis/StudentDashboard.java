    private void stUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stUpdateMouseClicked
        try {

            String Address=setAddress.getText();
            String City=setCity.getText();
            String Email=setEmail.getText();
            String Telephone=setPhone.getText();


            insert=conn1.prepareStatement("update  students set address_l1=?,address_l2=?,phone_num=? where index_num='"+userID+"'");

            insert.setString(1,Address );
            insert.setString(2, City);           
            insert.setString(3,Telephone );

            update=conn1.prepareStatement("update  student_email set se_email=? where se_st_id='"+userID+"'");
            update.setString(1, Email);

            if( Address.isEmpty() || City.isEmpty() || Email.isEmpty() || Telephone.isEmpty()){
                    JOptionPane.showMessageDialog(this,"Please Fill the all the fields!!");
            }else{
                insert.executeUpdate();
                update.executeUpdate();

                JOptionPane.showMessageDialog(this,"Recode Update!!");
                student_setting();
            }      


        } catch (SQLException ex) {
             Logger.getLogger(StudentDashboard.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_stUpdateMouseClicked

