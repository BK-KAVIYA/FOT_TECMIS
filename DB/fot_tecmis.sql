--
-- Structure for view `attendance_summary`
--
DROP TABLE IF EXISTS `attendance_summary`;

DROP VIEW IF EXISTS `attendance_summary`;
CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `attendance_summary`  AS  select `attendance`.`atte_st_id` AS `Index_Number`,`students`.`f_name` AS `First_Name`,`students`.`l_name` AS `Last_Name`,count(`attendance`.`atte_level`) AS `Student_Attendance` from (`attendance` left join `students` on((`attendance`.`atte_st_id` = `students`.`index_num`))) where (`attendance`.`atte_level` = 1) group by `attendance`.`atte_st_id` ;

-- --------------------------------------------------------

--
-- Structure for view `ca_summary`
--
DROP TABLE IF EXISTS `ca_summary`;

DROP VIEW IF EXISTS `ca_summary`;
CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `ca_summary`  AS  select `students`.`f_name` AS `f_name`,round((sum(`exam_marks`.`exm_score`) / count(`exam_marks`.`exm_score`)),0) AS `CA_Average` from (`exam_marks` left join `students` on((`exam_marks`.`exm_st_id` = `students`.`index_num`))) where (`exam_marks`.`exm_type` = 'Q') group by `exam_marks`.`exm_st_id` ;
