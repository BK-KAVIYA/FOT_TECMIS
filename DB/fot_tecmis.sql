--
-- Structure for view `allstattendanceandca`
--
DROP TABLE IF EXISTS `allstattendanceandca`;

DROP VIEW IF EXISTS `allstattendanceandca`;
CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `allstattendanceandca`  AS  select `students`.`index_num` AS `index_num`,`students`.`f_name` AS `f_name`,`students`.`l_name` AS `l_name`,`attendance`.`atte_level` AS `Attendance`,`exam_marks`.`exm_cmod_id` AS `exm_cmod_id`,`exam_marks`.`exm_score` AS `CA_Marks` from ((`students` join `attendance`) join `exam_marks`) where ((`students`.`index_num` = `attendance`.`atte_st_id`) and (`students`.`index_num` = `exam_marks`.`exm_st_id`) and (`exam_marks`.`exm_type` = 'Q')) group by `students`.`index_num` ;

